
// Variable declarations

// let
// - used to create varaible
// - create mutable variables
let num = 100
console.log('num = ',num)

num = 200
console.log('num = ',num)

// const
// - used to create constant
// - create immutable varibles
const pi = 3.14
console.log('pi = ',pi)
// pi = 3.15 // can not change a value
